package org.xyafu.pojo;

public class ImageMessage {
    private String imageId,imageAnimeName,imageRole,imageTags;

    public String getImageId() {
        return imageId;
    }

    public void setImageId(String imageId) {
        this.imageId = imageId;
    }

    public String getImageAnimeName() {
        return imageAnimeName;
    }

    public void setImageAnimeName(String imageAnimeName) {
        this.imageAnimeName = imageAnimeName;
    }

    public String getImageRole() {
        return imageRole;
    }

    public void setImageRole(String imageRole) {
        this.imageRole = imageRole;
    }

    public String getImageTags() {
        return imageTags;
    }

    public void setImageTags(String imageTags) {
        this.imageTags = imageTags;
    }
}
